#Method 1 - Using Exponents (The easy Way)
Number1 = 8
sqrt1 =  Number1 ** 0.5
print(f"The square root of{Number1} is {sqrt1:.4f}")

#Method 2 - Using math function
import math
Number2 = 8
sqrt2 = math.sqrt(Number2)
print(f"Using Math {Number2} is {sqrt2:.4f}")

#Method 3 - Using Pow
Number3 = 8
sqrt3 = pow(Number3,0.5)
print(f"Using pow{Number3} is {sqrt3:.4f}")





